<?php

    $a = 5;
    $b = 3;

    //Operador de Igualdade
    if($a == $b) {
        echo "A é igual a B - 1<br>";
    }

    else{
        echo "A é diferente de B - 1-2<br>";
    }

    // Operador de Diferenca
    if($a != $b) {
        echo "A é diferente de B - 2 <br>";
    }

    // Operador de Identico a
    if($a === $b) {
        echo "A é identico a B 3<br>";
    }

    else {
        echo "A não é identico a B 3-2<br>";
    }

    // Operador não Identico a
    if($a !== $b) {
        echo "A não é identico a B 4<br>";
    }

?>